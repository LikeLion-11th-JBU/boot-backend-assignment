public class Constants {
             //상수
    public static void main(String[] args){

        // 상수(constants, final variables)
        // 상수는 한 번 값이 할당되면 재할당이 불가능하다.
        // 전체를 대문자로 선언, 단어간 구분은 _(under score)로 활용.
        // Ex) PI_VALUE (under score) 활용한 예...


        //상수 선언 방법
        final double PI = 3.14;
        System.out.println(PI);

        // PI = 3.141592; // 이렇게 하면 error 가 뜸.
    }
}
