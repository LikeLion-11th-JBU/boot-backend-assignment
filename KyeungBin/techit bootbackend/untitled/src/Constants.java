public class Constants {

    public static void main(String[] args) {

        // 상수 (constants, final variables)

        // 1. 한 번 값이 할당되면, 이후 재할당이 불가능함.
        // 2. 전체를 대문자로 표시하고, 단어간 구분은 _ 로 함.
        //


        final double PI_VALUE = 3.14;
        System.out.println(PI_VALUE);



    }
}
